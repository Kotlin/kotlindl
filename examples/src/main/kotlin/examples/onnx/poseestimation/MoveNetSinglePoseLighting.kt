/**
 * This examples demonstrates the inference concept on SSD model:
 * - Model is obtained from [ONNXModelHub].
 * - Model predicts on a few images located in resources.
 * - Special preprocessing is applied to images before prediction.
 */
fun moveNetRawPrediction() {
    val modelHub = ONNXModelHub(cacheDirectory = File("cache/pretrainedModels"))
    val modelType = ONNXModels.PoseEstimation.MoveNetSinglePoseLighting
    val model = modelHub.loadModel(modelType)

    model.use {
        println(it)

        val imageFile = getFileFromResource("datasets/poses/1.jpg")
        val preprocessing: Preprocessing = preprocess {
            load {
                pathToData = imageFile
                imageShape = ImageShape(null, null, 3)
            }
            transformImage {
                resize {
                    outputHeight = 192
                    outputWidth = 192
                }
                convert { colorMode = ColorMode.BGR }
            }
        }

        val inputData = modelType.preprocessInput(preprocessing)

        val yhat = it.predictRaw(inputData)
        println(yhat.toTypedArray().contentDeepToString())
        visualisePosePoints(imageFile, (yhat as List<Array<Array<Array<FloatArray>>>>)[0][0][0])
        // Dictionary that maps from joint names to keypoint indices.
        val keypoints = {
            "nose" to 0
            "left_eye" to 1
            "right_eye" to 2
            "left_ear" to 3
            "right_ear" to 4
            "left_shoulder" to 5
            "right_shoulder" to 6
            "left_elbow" to 7
            "right_elbow" to 8
            "left_wrist" to 9
            "right_wrist" to 10
            "left_hip" to 11
            "right_hip" to 12
            "left_knee" to 13
            "right_knee" to 14
            "left_ankle" to 15
            "right_ankle" to 16
        }

    }
}


